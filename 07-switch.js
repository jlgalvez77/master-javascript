'use strict';

// SWITCH
var edad = 18;

switch (edad) {
    case 18:
        console.log("Acabas de cumplir la mayoria de edad");
        break;
    case 25:
        console.log("Ya eres un adulto");
        break;
    case 40:
        console.log("Crisis de los 40");
        break;
    case 75:
        console.log("Eres un anciano");
        break;
    default:
        console.log("Tu edad es neutra");
        break;
}